# Comparison Operators aise operators hain jo do cheezon (numbers, values ya variables) ka mukabla (comparison) 
# karte hain aur true ya false ka result dete hain. Yeh zyada tar if conditions me istemal hote hain.

a = 10
b = 20

print(a == b)  # False
print(a != b)  # True
print(a > b)   # False
print(a < b)   # True
print(a >= b)  # False
print(a <= b)  # True
