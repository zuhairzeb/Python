# String (str) Kya Hai?
# String ek text hota hai jo " " (double quotes) ya ' ' (single quotes) ke andar likha jata hai.

# Aasan Lafzon Mein:

# Jo bhi likha jaye aur quotes mein ho, wo string hoti hai.
# Yeh alfaz (words), jumlay (sentences), aur hatta kay numbers bhi store kar sakti hai, magar as a text.

# Example:
name = "Muhammad"
message = 'Hello, how are you?'

print(name, message)
