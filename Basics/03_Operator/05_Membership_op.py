# Python mein membership operators wo hotay hain jo check karte hain ke koi value kisi list, 
# string, ya tuple ka hissa hai ya nahi. Yeh do tarah ke hotay hain:

# in operator →  Check karta hai ke koi value kisi object ke andar mojood hai ya nahi.
# not in operator →  Check karta hai ke koi value kisi object ke andar nahi hai.

# Example:


fruits = ["apple", "banana", "cherry"]

print("apple" in fruits)    # True (kyunki "apple" list mein hai)
print("mango" not in fruits) # True (kyunki "mango" list mein nahi hai)