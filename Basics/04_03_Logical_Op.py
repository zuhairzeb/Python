# Logical Operators aise operators hain jo do ya zyada conditions ko aik sath check karte hain aur
#  True ya False ka result dete hain.
#  Yeh zyadatar if conditions me istemal hote hain.

# and

print(5 > 3 and 10 > 5)  # True
print(5 > 3 and 10 < 5)  # False


# or

print(5 > 3 or 10 < 5)  # True
print(5 < 3 or 10 < 5)  # False


# not

print(not (5 > 3))  # False
print(not (5 < 3))  # True
