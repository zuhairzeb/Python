# Conditional Statements: if, elif, else
  # Conditional statements ka matlab hai kisi condition (shart) ke mutabiq faisla lena. 
  # Python me if, elif, else ka use hota hai decision making ke liye.

    # if: Executes a block of code if the condition is true.

    #  elif: Checks another condition if the previous condition(s) were false.

    #   else: Executes a block of code if all previous conditions were false.

       
# Example:

age=int(input("enter your age :-"))
if age < 18: 
    print("You are a minor.")
elif age == 18:
   print("You just turned 18!")
else: 
 print("You are an adult.")



